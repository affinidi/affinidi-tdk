/// Enum representing different permission levels for resource access.
///
/// - [read] - grants read-only access to resources
/// - [write] - grants write-only access to resources
/// - [all] - grants full access (read and write) to resources
enum Permissions {
  /// Read-only access permission.
  read,

  /// Write-only access permission.
  write,

  /// Full access permission (read and write).
  all,
  ;
}
